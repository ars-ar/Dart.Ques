//Write a Dart program that uses a while loop to print numbers from 1 to 3. Each number should be printed on a new line.
void main() {
  // Initialize the counter variable
  int counter = 1;

  // Print numbers from 1 to 3 using a while loop
  while (counter <= 3) {
    // Print the current value of the counter
    print(counter);
    
    // Increment the counter by 1
    counter++;
  }
}
